package com.study.ch04;

public class Operation09 {
    public static void main(String[] args) {
        /*

        << 증강연산자 >>

         */

        int i = 0;
        System.out.println(i); //++i 선증강연산 i++ 후증강연산
        System.out.println(++i);
        System.out.println(i--); //대부분 후증간연산
        System.out.println(i);

    }
}
